namespace Interrupt {
	//-------------------------------------------------------------------------
	// ● 初始化
	//-------------------------------------------------------------------------
	void initialize() {
		ASM::out8(0x21, 0xff);
		ASM::out8(0xa1, 0xff);
		ASM::out8(0x20, 0x11);
		ASM::out8(0x21, 0x20);
		ASM::out8(0x21, 0x04);
		ASM::out8(0x21, 0x01);
		ASM::out8(0xa0, 0x11);
		ASM::out8(0xa1, 0x28);
		ASM::out8(0xa1, 0x02);
		ASM::out8(0xa1, 0x01);
		ASM::out8(0x21, 0b11111011);
		ASM::out8(0xa1, 0b11111111);
	}
}
